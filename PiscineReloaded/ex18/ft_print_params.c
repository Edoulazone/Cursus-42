#include <unistd.h>

void	ft_putchar(char c);

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 1;
	while (i < ac)
	{
		j = 0;
		while (av[i][++j])
			ft_putchar(ac[i][j]);
		ft_putchar("\n");
		i++;
	}
	return (0);
}
